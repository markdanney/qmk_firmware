bool process_record_user(uint16_t keycode, keyrecord_t *record) {
    switch (keycode) {
            /* KEYBOARD PET STATUS START */

        case KC_LCTL:
            isSneaking = record->event.pressed;
            break;
        case KC_SPC:
            isJumping = record->event.pressed;
            if (isJumping) {
                showedJump = false;
            }
            break;
        case KC_CAPS:
            isBarking = record->event.pressed;
            break;

            /* KEYBOARD PET STATUS END */
    }

    return true;
}
